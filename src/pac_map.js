const make_map = (wall_color) => {

  const inner_box_size = box_size / lines_thickness;
  const offset_size = box_size - inner_box_size;

  const createWalls = () => {
    for (let i = 0; i < map.length; i++) {
      for (let j = 0; j < map[0].length; j++) {
        if (map[i][j] === 1) {
          //its a wall
          displayWall(
            j * box_size,
            i * box_size,
            box_size,
            box_size,
            wall_color
          );
          displayWall(
            j * box_size + offset_size / 2,
            i * box_size + offset_size / 2,
            inner_box_size,
            inner_box_size,
            "black"
          );
          if (j > 0 && map[i][j - 1] === 1) {
            displayWall(
              j * box_size - offset_size,
              i * box_size + offset_size / 2,
              inner_box_size + offset_size * 1.3,
              inner_box_size,
              "black"
            );
          }
          if (i > 0 && map[i - 1][j] === 1) {
            displayWall(
              j * box_size + offset_size / 2,
              i * box_size - offset_size,
              inner_box_size,
              inner_box_size + offset_size * 1.3,
              "black"
            );
          }
        }
      }
    }
  };
  const displayWall = (x, y, width, height, color) => {
    canvas.fillStyle = color;
    canvas.fillRect(x, y, width, height);
  };

  createWalls();
};
